#include "main.h"

/**
 * add - adds two integers and returns the result
 * @a: first integer
 * @b: second integer
 *
 * Return: the sum of a and b
 */

int add(int a, int b)
{
	// Calcule la somme de 'a' et 'b', et renvoie le résultat
	// à l'endroit où la fonction a été appelée.
	return (a + b);
}
